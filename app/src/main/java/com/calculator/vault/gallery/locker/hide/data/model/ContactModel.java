package com.calculator.vault.gallery.locker.hide.data.model;

public class ContactModel {
    int ID;
    String contactFirstName;
    String contactLastName;
    String contactFullName;
    String contactPhoneNumber;
    String contactEmail;
    String contactBirthDate;
    String contactFilePath;
    int contactColor;
    String contactID;

    public String getContactID() {
        return contactID;
    }

    public void setContactID(String contactID) {
        this.contactID = contactID;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getContactFullName() {
        return contactFullName;
    }

    public void setContactFullName(String contactFullName) {
        this.contactFullName = contactFullName;
    }

    public String getContactFirstName() {
        return contactFirstName;
    }

    public void setContactFirstName(String contactFirstName) {
        this.contactFirstName = contactFirstName;
    }

    public String getContactLastName() {
        return contactLastName;
    }

    public void setContactLastName(String contactLastName) {
        this.contactLastName = contactLastName;
    }

    public String getContactPhoneNumber() {
        return contactPhoneNumber;
    }

    public void setContactPhoneNumber(String contactPhoneNumber) {
        this.contactPhoneNumber = contactPhoneNumber;
    }

    public String getContactEmail() {
        return contactEmail;
    }

    public void setContactEmail(String contactEmail) {
        this.contactEmail = contactEmail;
    }

    public String getContactBirthDate() {
        return contactBirthDate;
    }

    public void setContactBirthDate(String contactBirthDate) {
        this.contactBirthDate = contactBirthDate;
    }

    public String getContactFilePath() {
        return contactFilePath;
    }

    public void setContactFilePath(String contactFilePath) {
        this.contactFilePath = contactFilePath;
    }

    public int getContactColor() {
        return contactColor;
    }

    public void setContactColor(int contactColor) {
        this.contactColor = contactColor;
    }
}
